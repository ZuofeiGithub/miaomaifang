package com.huiketong.sumaifang.data;

public class PrivatePhoneData {

    /**
     * user_phone : consectetur occaecat laboris velit
     * expiration_time : nisi cupidatat proident sint occaecat
     * buyer_phone : cupidatat consequat deserunt
     */

    private String user_phone;
    private String expiration_time;
    private String buyer_phone;

    public String getUser_phone() {
        return user_phone;
    }

    public void setUser_phone(String user_phone) {
        this.user_phone = user_phone;
    }

    public String getExpiration_time() {
        return expiration_time;
    }

    public void setExpiration_time(String expiration_time) {
        this.expiration_time = expiration_time;
    }

    public String getBuyer_phone() {
        return buyer_phone;
    }

    public void setBuyer_phone(String buyer_phone) {
        this.buyer_phone = buyer_phone;
    }
}
