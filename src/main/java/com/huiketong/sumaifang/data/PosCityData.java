package com.huiketong.sumaifang.data;

public class PosCityData {

    /**
     * cityname : consequat
     * isopen : dolore esse proident
     * default_cityname : ut et
     */

    private String cityname;
    private String isopen;
    private String default_cityname;

    public String getCityname() {
        return cityname;
    }

    public void setCityname(String cityname) {
        this.cityname = cityname;
    }

    public String getIsopen() {
        return isopen;
    }

    public void setIsopen(String isopen) {
        this.isopen = isopen;
    }

    public String getDefault_cityname() {
        return default_cityname;
    }

    public void setDefault_cityname(String default_cityname) {
        this.default_cityname = default_cityname;
    }
}
